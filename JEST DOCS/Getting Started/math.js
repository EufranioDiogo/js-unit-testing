const sum = (number1 = 0, number2 = 0) => number1 + number2;
const multiply = (number1 = 0, number2 = 0) => number1 * number2;
const division = (number1 = 0, number2 = 0) => number1 / number2;
const subtraction = (number1 = 0, number2 = 0) => number1 - number2;
const restDivision = (number1 = 0, number2 = 0) => number1 % number2;

module.exports = { sum, multiply, division, subtraction, restDivision };
